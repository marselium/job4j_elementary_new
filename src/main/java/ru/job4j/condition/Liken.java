package ru.job4j.condition;

public class Liken {
    public static void main(String[] args) {
        int fir = 10;
        int sec = 9;

        boolean rsl = fir > sec;
        System.out.println(rsl);

        rsl = fir < sec;
        System.out.println(rsl);

        rsl = fir == sec;
        System.out.println(rsl);
    }
}
